#include "initial.h"

#include "led.h"

//***************************************************************************
//! \brief       系统初始化函数
//! \param       无
//! \return      无
//***************************************************************************
void initial(void)
{
	u8 i;
//***************************************************************************
//! \brief       IO口设置
//***************************************************************************
		P0M		&=	0x38;
		P0M		|=	0x45;
		P0UR	|=	0x05;

		P4M 	&=	0xb8;
		P4M 	|=	0x40;
		P4UR	|=	0x00;

		P5M 	|=	0x0f;
		P5UR	|=	0x08;
//***************************************************************************
//! \brief       定时器T0设置
//***************************************************************************
		FT0IEN=0;
		FT0ENB=0;
		T0M=0x70;	//T0时钟=Fcpu/8  0x60->Fcpu/4  0x70->Fcpu/2
		T0C=0xCC;	//T0C初值 0xC2 125us
		FT0IRQ=0;
		FT0IEN=1;
		FT0ENB=1;
		FGIE=1;
//***************************************************************************
//! \brief       ADC设置
//***************************************************************************
		ADM = 0x00;		//清ADM寄存器
		ADR = 0x10;		// ADC时钟Rate = Fcpu/8    分辨率 8bit
		P4CON |= 0x18;	//选择ADC输入通道
		FADENB = 1; 	//使能ADC

		ADM |= 3;		//通道选择
		FGCHS = 1;		//使能ADC
		FADCIRQ = 0;	//清ADC中断请求
//		FADCIEN = 1;	//使能ADC中断功能
		FADS = 1;		//开始AD转换
//***************************************************************************
//! \brief       LED显示 - TM1628初始化
//***************************************************************************
		led_wr(0x02);
		led_wr(0x40);	// 0x44 固定地址
		led_wr(0xc0);
		for(i=0;i<14;i++)
		led_wrDATA(0x00);
		led_wr(0x8b);		// 88开显示  80关显示   +1~7调节亮度
//***************************************************************************
//! \brief       旋转电位器 引脚状态buf
//***************************************************************************
		ffp03 = FP03;		// P03引脚状态
		ffp05 = FP05;		// P05引脚状态
}
