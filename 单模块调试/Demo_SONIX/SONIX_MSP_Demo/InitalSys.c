/******************************************************************************************************
名  称:	   void	InitalSystem(void)
功能描述 ：初始化系统寄存器
******************************************************************************************************/
void	InitalSystem(void)
{
/*-----------------------------------------------------------------------------------------------------
	Inital	MSP
-----------------------------------------------------------------------------------------------------*/
	 MSPADR	= 0X05;		//MSP速率为400KHz,W/R = 0;
	 FMSPENB = 1;		//关闭MSP功能后，再次打开MSP必须重新初始化
	 FMSPC = 1;

/*-----------------------------------------------------------------------------------------------------
	Inital	I/O		open-drain mode
-----------------------------------------------------------------------------------------------------*/
	 P1M = 0XFF;
	 P1OC = 0X0C;
}