#include	"User.h"
#include 	"Key.h"
#include	"IntGnd.h"

extern 	bit		F1ms;
extern	bit		Fkeyin;
extern	uint	KeyChat;
/*-------------------------------------------------------------------------------------------------------*/
/**********************************************************************************************************
 名 称  ：	void	DeKeychat(void)
功能描述：  按键消抖动

***********************************************************************************************************/
void	DeKeychat(void)
{
	if(F1ms ==1)
	{
		F1ms = 0;
		
		if((Fkeyin == 1)&&(KeyChat != 0))
		{
			KeyChat--;		
		}
	}	
}