//---------------------------------------------------------------------------------------------------------
#include	"User.h"

#include	"Led.h"

extern		uint	KeyFIFO;
uint		Keynum;
//---------------------------------------------------------------------------------------------------------

/**********************************************************************************************************
按键扫描程序:	void	LedApp(void)
程  序 功 能：	按键键值转换为Led的亮或灭
**********************************************************************************************************/
void	LedApp(void)
{
 	if(KeyFIFO == 0X01)
	{
		KeyFIFO = 0X00;
	
		if((Keynum)^(0X01) == 1)
		{
			 Led = 1;						//点亮
		}
		else Led = 0;						//关闭

		Keynum++;
	}

	if(Keynum == 2)
	{
		Keynum = 0;
	}
}